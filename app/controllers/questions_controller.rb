class QuestionsController < ApplicationController
  # GET /questions
  # GET /questions.json
  def index
    filtered = !params[:dimensions].nil?
    if filtered
      @research = Research.find(params[:research_id])
      @dimensions = Dimension.where(:id=>params[:dimensions])
      @questions = Question.where(:dimension_id=>params[:dimensions])
    else
      @questions = Question.all
    end

    respond_to do |format|
      if filtered
        format.html{ render :partial=>"multiselect_for_questions", :layout=>false}
      else
        format.html # index.html.erb
      end
      format.json { render json: @questions }
    end
  end

  # GET /questions/1
  # GET /questions/1.json
  def show
    @question = Question.find(params[:id])

    respond_to do |format|
      format.html # show.html.erb
      format.json { render json: @question }
    end
  end

  # GET /questions/new
  # GET /questions/new.json
  def new
    @question = Question.new
    @question.is_default = true
    respond_to do |format|
      format.html { render :layout=>@layout }# new.html.erb
      format.json { render json: @question }
    end
  end

  # GET /questions/1/edit
  def edit
    @question = Question.find(params[:id])
  end

  # POST /questions
  # POST /questions.json
  def create
    @question = Question.new(permited_params(:question))

    respond_to do |format|
      if @question.save
        format.html { redirect_to @question, notice: 'Question was successfully created.' }
        format.json { render json: @question, status: :created, location: @question }
      else
        format.html { render action: "new", :layout=>@layout  }
        format.json { render json: @question.errors, status: :unprocessable_entity }
      end
    end
  end

  # PUT /questions/1
  # PUT /questions/1.json
  def update
    @question = Question.find(params[:id])

    respond_to do |format|
      if @question.update_attributes(permited_params(:question))
        format.html { redirect_to @question, notice: 'Question was successfully updated.' }
        format.json { head :no_content }
      else
        format.html { render action: "edit" }
        format.json { render json: @question.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /questions/1
  # DELETE /questions/1.json
  def destroy
    @question = Question.find(params[:id])
    @question.destroy

    respond_to do |format|
      format.html { redirect_to questions_url }
      format.json { head :no_content }
    end
  end
end
